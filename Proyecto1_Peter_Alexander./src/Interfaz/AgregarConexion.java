    private void agregarParadasRed(){
        ListaSimple nombres = FuncionesApp.listaParadas(redTransporte, 2);
        for (int i = 0; i < nombres.getSize(); i++) {
            modeloParadas.addElement(nombres.getValor(i));
        }
    }
    
    private void agregarParadasLinea(){
        ListaSimple nombres = FuncionesApp.nombresVertices(verticesNuevos);
        for (int i = 0; i < nombres.getSize(); i++) {
            modeloParadasLinea.addElement(nombres.getValor(i));
        }
    }

