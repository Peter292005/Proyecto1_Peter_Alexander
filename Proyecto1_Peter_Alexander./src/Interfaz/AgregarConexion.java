    private void agregarConexionActionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarConexionActionActionPerformed
        String nombreParadaLineaNueva = (String) paradasLineaNueva.getSelectedItem();
        String nombreParadaRed = (String) paradaRed.getSelectedItem();
        
        FuncionesApp.agregarVertices(redTransporte, verticesNuevos);
        
            Vertice verticeInicio = redTransporte.buscarVerticePorNombre(nombreParadaRed);
            Vertice verticeFinal = redTransporte.buscarVerticePorNombre(nombreParadaLineaNueva);
            
            redTransporte.conectarVertices(verticeInicio, verticeFinal);
            
            JOptionPane.showMessageDialog(null, "Conexion de la Linea Nueva con el Grafo realizada con exito.");
            verticesNuevos.destruir();
            
            Menu menu = new Menu();
            this.dispose();
    }//GEN-LAST:event_agregarConexionActionActionPerformed

